    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        try {
            Connection k = Koneksi.Go();
            Statement st = k.createStatement();
            int res = st.executeUpdate("UPDATE alternatif SET id = '" + txID.getText() + "', nama_laptop = '" + txNama.getText() + "', merk = '" + txMerek.getText() + "', tahun_rilis = '" + txTahun.getText()  + "' WHERE id = " + fieldID.getText());
            if(res > 0){
                AlternatifFrame.refreshData();
                JOptionPane.showMessageDialog(this, "Berhasil Memperbarui Data");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal Memperbarui Data");
                this.dispose();
            }
        } catch (HeadlessException | SQLException e) {
            System.out.println(e.getMessage());
        } 
    }//GEN-LAST:event_btnSubmitActionPerformed

