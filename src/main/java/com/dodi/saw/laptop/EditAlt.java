    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            Connection k = Koneksi.Go();
            Statement st = k.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM alternatif WHERE id = " + fieldID.getText());

            if (rs.next()) {
                txID.setText(rs.getString("id"));
                txNama.setText(rs.getString("nama_laptop"));
                txMerek.setText(rs.getString("merk"));
                txTahun.setText(rs.getString("tahun_rilis"));
            } else {
                JOptionPane.showMessageDialog(this, "ID tidak ditemukan, silahkan coba lagi");
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btnSearchActionPerformed

